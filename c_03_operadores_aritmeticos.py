# Operadores Aritméticos

# Operadores aritméticos básicos en Python
a = 10
b = 3

suma = a + b
print("Suma:", suma)

resta = a - b
print("Resta:", resta)
multiplicacion = a * b
print("Multiplicación:", multiplicacion)
# División normal, devuelve un float
division = a / b
print("División:", division)
# División entera, devuelve valor entero redondeado hacia abajo como un int.
division_entera = a // b
print("División entera:", division_entera)

# Módulo, devuelve el resto de la división
modulo = a % b
print("Módulo:", modulo)

# Potencia, devuelve a elevado a la b
potencia = a**b
print("Potencia:", potencia)
